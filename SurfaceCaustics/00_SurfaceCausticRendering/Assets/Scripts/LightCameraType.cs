/// <summary>
/// Utility structure to maintain the type of cameras that are used in the system
/// </summary>
public enum LightCameraType
{
    /// <summary>
    /// Flag used to represent a camera which captures and stores position data
    /// </summary>
    POSITION = 0,

    /// <summary>
    /// Flag used to represent a camera which captures and stores normal vector data
    /// </summary>
    NORMAL = 0,
}
